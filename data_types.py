type_string = "x"
print(f'String type {type_string}')

type_int = 5
print(f'Int type {type_int}')

type_float = 3.5
print(f'Float type {type_float}')

type_complex = 1j
print(f'Complex {type_complex}')

type_list = ["Ana", "Banana"]
print(f'List: {type_list}')

type_tuple = ("Sexta", "Feira")
print(f'Tuple: {type_tuple}')

type_range = range(6)
print(f'Range: {type_range}')

type_dict = {'tz': "America/Sao_Paulo"}
print(f'Dict: {type_dict}')

type_set = {'apple', 'banana'}
print(f'Set: {type_set}')

type_frozenset = frozenset({"cherry", "pick"})
print(f'Frozenset: {type_frozenset}')

type_bool = True
print(f'Bool: {type_bool}')

type_bytes = b"Hello"
print(f'Bytes: {type_bytes}')

type_bytearray = bytearray(5)
print(f'Bytearray: {type_bytearray}')

type_memoryview = memoryview(bytes(5))
print(f'Memoryview: {type_memoryview}')

type_none = None
print(f'None: {type_none}')
